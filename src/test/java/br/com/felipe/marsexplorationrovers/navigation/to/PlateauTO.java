package br.com.felipe.marsexplorationrovers.navigation.to;

public class PlateauTO {
    private int x;
    private int y;

    public PlateauTO(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
